<h2 class="num one">What's the best hat shape for me?</h2>
<div class="desc">
	<p>If you've ever donned a hat you love only to find it doesn't love you back, you know the importance of matching the hat to the head.  For those fortunate enough to have an oval or triangular face, almost any hat looks good. For the rest of us, pay close attention.</p>
