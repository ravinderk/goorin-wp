<a name="glossary"></a>
<h2 class="num four">GLOSSARY</h2>
<div class="bounding">
	<dl><dt>Baseball cap</dt>
	<dd>A paneled cloth cap with a brim. The heroes of America's pastime made it famous in the early 1900s, and fans soon brought it off the field to make it the most popular casual style.</dd></dl>
	<dl><dt>Beanie</dt>
	<dd>A tight-fitting, casual knit cap that's cozy and protects from cold. The Canucks call it a tuque.</dd></dl>
	<dl><dt>Beret</dt><dd>A soft, wide, circular cap made from felt, felted jersey or fabric, sure to give the wearer an air of intelligence and timeless style. Très chic!</dd></dl>
	<dl><dt>Blocking</dt>
	<dd>The process by which most felt and straw hats are made. A hood (unshaped cone of material) is heated and pulled very tightly over a wooden or aluminum mold. After cooling, the hat is taken off the block and finished. And voila - the magic of hatmaking turns shapeless into stylish.</dd></dl>
	<dl><dt>Boater</dt>
	<dd>A flat-topped hat with a small flat brim, traditionally made of stiffened straw. Some call it a skimmer, but by any name this hat will always be the quintessential summer style.</dd></dl>
	<dl><dt>Bonnet</dt>
	<dd>A large headdress with a deep brim and ribbons to tie under the chin. Think Pride &amp; Prejudice and you'll have the right idea.</dd></dl>
	<dl><dt>Bowler</dt>
	<dd>An oval hat with a round, rigid crown and a small, curved brim. If you're stateside, be sure to call it a derby - bowlers are strictly British.</dd></dl>
	<dl><dt>Brim</dt>
	<dd>The projecting edge of a hat, sometimes called a peak. Keep it low down for that air of mystery or up a bit higher for unmistakable confidence.</dd></dl>
	<dl><dt>Brisa weave (BREE-sah)</dt>
	<dd>A basket-type weave used for Panama hats. This highly efficient method makes for an extra-breathable hat, the perfect choice for long hours in the sunshine.</dd></dl>
	<dl><dt>Buckram</dt>
	<dd>Stiff netting used to make hats. Look on the underside of that fancy fascinator and you'll probably find it's buckram anchoring the entire affair.</dd></dl>
	<dl><dt>Cap</dt>
	<dd>A hat with a small brim at the front. It could be an ivy, a Gatsby or a baseball - however you wear it, a cap is the perfect everyday choice.</dd></dl>
	<dl><dt>Cloche</dt>
	<dd>From the French for “bell”, a close-fitting hat featuring a round crown and either no brim or a narrow, flared one. Originating in the 1920s, it was so popular it could be found atop the flapper and the temperance campaigner alike.</dd></dl>
	<dl><dt>Cowboy hat</dt>
	<dd>A fedora-style hat with a high crown and wide brim originally worn by cowhands in the American West.</dd></dl>
	<dl><dt>Crown</dt>
	<dd>The top part of a hat. It should be comfortably snug without fitting too tight - a headache is no one's friend.</dd></dl>
	<dl><dt>Cut &amp; sew</dt>
	<dd>Hats sewn together from pieces of fabric using an existing pattern. Cut &amp; sew describes most hats that aren't created using blocking.</dd></dl>
	<dl><dt>Deerstalker</dt>
	<dd>A hunting cap with visors at the front and back and earflaps that can be tied up over the head. Put one on and you might find yourself doing some sleuthing - after all, it's Sherlock Holmes' signature hat.</dd></dl>
	<dl><dt>Doff</dt>
	<dd>Partially removing a hat as a sign of respect or greeting, the perfect way to say hello to friends and neighbors as you go about your day.</dd></dl>
	<dl><dt>Don</dt>
	<dd>Putting a hat on one's head, something you must remember to do after doffing it to passerby.</dd></dl>
	<dl><dt>Diadem</dt>
	<dd>A jeweled headpiece that's halfway between a tiara and a headband and fit for regal occasions.</dd></dl>
	<dl><dt>Fascinator</dt>
	<dd>An elaborately trimmed hair decoration on a band, clip or comb. Slip one on to fit right in with British royalty at the Royal Ascot horse races.</dd></dl>
	<dl><dt>Fedora</dt>
	<dd>A brimmed hat with a tapered crown dented down the top. Named after the lead character in Victorien Sardou's 1882 drama Fedora, it has remained the Western world's favorite shape since its debut.</dd></dl>
	<dl><dt>Felt</dt>
	<dd>Cloth made from wool, fur or hair, compacted (felted) by rolling and pressing in the presence of heat and moisture.</dd></dl>
	<dl><dt>Gibus</dt>
	<dd>A collapsible top hat named for its inventor, Antoine Gibus, whose hinged model solved the 1800s problem of bulky top hats crowding up coat check at the opera. For this reason, they are sometimes called opera hats.</dd></dl>
	<dl><dt>Grosgrain (GROW-grain)</dt>
	<dd>Strong, heavy corded fabric characterized by its ribbed appearance. It is made from wool, silk, or a combination of fibers and is most commonly used for ribbon - including the fine bands on many of our styles.</dd></dl>
	<dl><dt>Harris tweed</dt>
	<dd>Hand-woven tweed from the Outer Hebrides (HEH-brih-deez) Isles in Scotland. Known as the “Champagne of fabrics”, it is protected by an Act of Parliament that oversees use of the Harris Orb label to ensure authenticity.</dd></dl>
	<dl><dt>Homburg</dt>
	<dd>Formal hat made of felt with a narrow, upturned brim and a depression in the top.</dd></dl>
	<dl><dt>Hood</dt>
