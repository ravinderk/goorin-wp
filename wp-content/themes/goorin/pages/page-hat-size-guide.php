<h2 class="num two">WHAT'S MY HAT SIZE?</h2>
<figure class="chart">
	<style>.hatsize figure.chart img { box-shadow:none } </style>
	<img alt="Sizing Chart" src="http://media.goorin.com/media/wysiwyg/fitguide/chart_fitguide2.png">
</figure>
<div class="desc">
	<h3>Everyone knows their shoe size.</h3>
	<p>Hats? Not so much. But with a few quick measurements, you will know your hat size which, unlike your waist size, will stay the same for the rest of your life. All you need is a cloth measuring tape or a piece of string and a ruler.</p>
	<p>Simply measure the circumference of your head in inches or centimeters, holding the tape or string snug, but not too tight, just above your ears - which is right about where your hat will sit. The job is made easier if you can recruit the neighbor or a friendly cop to take the measurements for you. If none is available, use a mirror and measure several times to ensure accuracy. Then consult the chart below to determine your equivalent size.</p>
